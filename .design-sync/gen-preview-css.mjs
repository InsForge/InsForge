#!/usr/bin/env node
// Compile a self-contained preview stylesheet for @insforge/ui.
//
// @insforge/ui ships only design TOKENS (dist/styles.css). Its visual styling
// comes from Tailwind v4 utility classes in the components' className strings,
// generated at the CONSUMING app's build time via the package's tailwind-preset
// (see packages/dashboard/tailwind.config.js). design-sync's bundle is just the
// compiled JS — so without this step every preview and every design built with
// the DS renders unstyled.
//
// This reproduces what the real consumer (the dashboard) generates: it scans
// packages/ui/src for utility classes and compiles them against the package's
// own tailwind-preset (which maps the color tokens) + the standard Tailwind
// theme, prepended with the token :root/.dark definitions. The output is fully
// self-contained (tokens + preflight + utilities) and is written INSIDE the
// package so cfg.cssEntry (bounded to the package dir) can point at it.
//
// Re-run after `npm run build` whenever the UI source changes. Deterministic.
import { compile } from '@tailwindcss/node';
import { Scanner } from '@tailwindcss/oxide';
import { readFileSync, writeFileSync } from 'node:fs';
import { dirname, join, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const HERE = dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = resolve(HERE, '..');
const UI_DIR = join(REPO_ROOT, 'packages', 'ui');
const UI_SRC = join(UI_DIR, 'src');
const TOKENS_CSS = join(UI_DIR, 'dist', 'styles.css'); // token :root/.dark defs
const OUT_CSS = join(UI_DIR, 'dist', 'ds-preview.css'); // cfg.cssEntry target

// Strip CSS block comments from the token file. Its doc header contains a
// literal `@import '@insforge/ui/styles.css';` example that would otherwise
// trip validate's [CSS_IMPORT_MISSING] scan (it matches @import in comments).
const tokens = readFileSync(TOKENS_CSS, 'utf8').replace(/\/\*[\s\S]*?\*\//g, '').trim();

// Tailwind v4 input. @config loads the package's preset (color → var() maps)
// plus the dashboard's theme extend + tailwindcss-animate, so the compiled
// utilities match exactly what the real app produces for these components.
const input = `@import 'tailwindcss';
@config "${join(HERE, 'preview-tailwind.config.cjs')}";
`;

const compiler = await compile(input, {
  base: UI_DIR,
  onDependency: () => {},
});

const scanner = new Scanner({
  sources: [{ base: UI_SRC, pattern: '**/*.{ts,tsx}', negated: false }],
});
const candidates = scanner.scan();

const utilitiesCss = compiler.build(candidates);

// The DS's brand font (Inter) is not shipped with the package — InsForge apps
// load it from Google Fonts at runtime (see frontend/index.html). Pull it in the
// same way so previews and generated designs render in real Inter. A remote
// @import must lead the file (before any other rule), so it goes first.
const FONT_IMPORT = `@import url('https://fonts.googleapis.com/css2?family=Inter:wght@400;500;600;700&display=swap');`;

// Prepend token definitions so the file is self-contained (designs receive
// only this file's closure). Font import first, then tokens, then preflight + utilities.
const out = `${FONT_IMPORT}\n/* Generated by .design-sync/gen-preview-css.mjs — do not edit by hand. */\n/* InsForge UI design tokens (from dist/styles.css) */\n${tokens}\n/* Tailwind preflight + compiled @insforge/ui utilities */\n${utilitiesCss}\n`;

writeFileSync(OUT_CSS, out);
console.error(`gen-preview-css: wrote ${OUT_CSS} (${(out.length / 1024).toFixed(0)} KB, ${candidates.length} candidates)`);
